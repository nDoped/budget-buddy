<script setup>
  import { computed } from 'vue';
  import { Link } from '@inertiajs/vue3';

  const props = defineProps({
    href: {
      type: String,
      default: () => null,
    },
    active: {
      type: Boolean,
      default: () => null
    }
  });

  const classes = computed(() => {
    return props.active
      ? 'inline-flex items-center px-1 pt-1 border-b-2 border-indigo-400 text-sm font-medium leading-5 text-gray-100 focus:outline-none focus:border-indigo-700 transition'
      : 'inline-flex items-center px-1 pt-1 border-b-2 border-transparent text-sm font-medium leading-5 text-slate-900 hover:text-gray-700 hover:border-gray-300 focus:outline-none focus:text-gray-700 focus:border-gray-300 transition';
  });
</script>

<template>
  <Link
    :href="href"
    :class="classes"
  >
    <slot />
  </Link>
</template>
